
# Operadores

"""
Soma +
Substração -
multiplicação *
divisão /
divisão inteira //
resto da divisão %
potenciação **
"""

numero1 = 10
numero2 = 20

print(numero1+numero2)
print(numero1-numero2)
print(numero1*numero2)
print(numero1/numero2) # divisão normal
print(numero1//numero2) # divisão que retorna resultado inteiro, despreza os valores decimais, não é arredondamento
print(numero1%numero2) # apresenta o resto da divisão
print(20/3)
print(20//3)
print(20%3)
print(20**3)


